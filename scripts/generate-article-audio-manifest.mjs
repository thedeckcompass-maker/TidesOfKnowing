/**
 * Scans public/audio/articles/[slug]/article.mp3 and writes
 * src/generated/articleAudioSlugs.ts for edge-safe lookup at runtime.
 */
import { existsSync, mkdirSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const articlesAudioDir = join(root, "public", "audio", "articles");
const outPath = join(root, "src", "generated", "articleAudioSlugs.ts");

const slugs = [];

if (existsSync(articlesAudioDir)) {
  for (const name of readdirSync(articlesAudioDir)) {
    const dirPath = join(articlesAudioDir, name);
    if (name === "README.md") continue;
    try {
      if (!statSync(dirPath).isDirectory()) continue;
    } catch {
      continue;
    }
    if (existsSync(join(dirPath, "article.mp3"))) {
      slugs.push(name);
    }
  }
}

slugs.sort();

const file = `/** Generated by scripts/generate-article-audio-manifest.mjs — do not edit. */
export const ARTICLE_AUDIO_SLUGS: readonly string[] = ${JSON.stringify(slugs)};
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, file, "utf8");
